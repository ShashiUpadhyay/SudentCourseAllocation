
public class Student {

	private int subID;
	private int preferenceUsed;
	private int preferenceGiven;
	private int maxEnrollment;
	private int subjectCount;
	
	private int maxPreferenceUsed;
	
	public int getMaxPreferenceUsed() {
		return maxPreferenceUsed;
	}
	public void setMaxPreferenceUsed(int maxPreferenceUsed) {
		this.maxPreferenceUsed = maxPreferenceUsed;
	}
	public int getMaxEnrollment() {
		return maxEnrollment;
	}
	public void setMaxEnrollment(int maxEnrollment) {
		this.maxEnrollment = maxEnrollment;
	}
	public int getSubjectCount() {
		return subjectCount;
	}
	public void setSubjectCount(int subjectCount) {
		this.subjectCount = subjectCount;
	}
	public int getPreferenceGiven() {
		return preferenceGiven;
	}
	public void setPreferenceGiven(int preferenceGiven) {
		this.preferenceGiven = preferenceGiven;
	}
	
	public int getPreferenceUsed() {
		return preferenceUsed;
	}
	public void setPreferenceUsed(int preferenceUsed) {
		this.preferenceUsed = preferenceUsed;
	}
	public int getSubID() {
		return subID;
	}
	public void setSubID(int subID) {
		this.subID = subID;
	}
	
}
